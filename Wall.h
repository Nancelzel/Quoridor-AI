#ifndef WALL_H_
#define WALL_H_

// Represents a wall
struct Wall {
  // Starting x coordinate of wall
  int sX;

  // Starting y coordinate of wall
  int sY;

  // Middle x coordinate of wall
  int mX;

  // Middle y coordinate of wall
  int mY;

  // Ending x coordinate of wall
  int eX;

  // Ending y coordinate of wall
  int eY;
};

#endif
